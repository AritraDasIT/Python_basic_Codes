a = int(input("Enter a: "))
b = int(input("Enter b: "))


print("Addition of the two values is", a + b)
print("Subtraction of the two values is", a - b)
print("Multiplication of the two values is", a * b)
print("Division of the two values is", a / b)
print("Modulus of the two values is", a % b)       #Modulus
print("Exponent of the two values is", a ** b)       #Exponent
print("Floor division of the two values is", a // b)  #Floor