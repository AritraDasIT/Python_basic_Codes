#The conversion of one data type into the other data type is known as type casting in python or type conversion in python. Python supports a wide variety of functions or methods like: int(), float(), str(), ord(), hex(), oct(), tuple(), set(), list(), dict(), etc. for the type casting in pytho

#Explicit typecasting:
# Explicit type conversion takes place when the programmer clearly and explicitly defines the same in the program.

A= "15";
B= 8;

print(int(A)+B);  

#Implicit typecasting:
#In Python, when the data type conversion takes place during compilation or during the runtime, it's called an implicit data type conversion. Python handles the implicit data type conversion.

a=10;
b=1.5;
print(a+b);

