# STRING SLICING AND OPERATION ON STRING

# LENGTH OF A STRING

Name="My name is Aritra";
print(Name);

print(len(Name));  #use { len } function 

print(Name[0:8]);  #character access & STRING SLICING ||  #including 0 but not 8

# Negative STRING SLICING

print(Name[0:-8]);   # this means 
print(Name[0:len(Name)-8]);     

print(Name[-8:-2]);

NAME= "Harry";

print(NAME[-4:-2]);


